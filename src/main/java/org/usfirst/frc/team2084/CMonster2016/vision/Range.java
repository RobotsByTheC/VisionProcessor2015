package org.usfirst.frc.team2084.CMonster2016.vision;

/**
 *
 * @author Ben Wolsieffer
 */
public class Range {

    private int min;
    private int max;

    public Range(int min, int max) {
        this.min = min;
        this.max = max;
    }

    public int getMax() {
        return max;
    }

    public int getMin() {
        return min;
    }

    public void setMax(int max) {
        this.max = max;
    }

    public void setMin(int min) {
        this.min = min;
    }

    @Override
    public String toString() {
        return min + "," + max;
    }

}
